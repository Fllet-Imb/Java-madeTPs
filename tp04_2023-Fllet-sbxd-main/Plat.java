public class Plat {
    private String nom;
    private String typePlat;

    // Constructor
    public Plat(String nom, String typePlat) {
        this.nom = nom;
        this.typePlat = typePlat;
    }

    // Getters
    public String getNom() {
        return nom;
    }

    public String getTypePlat() {
        return typePlat;
    }
}
